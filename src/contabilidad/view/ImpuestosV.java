    /**
     * Este método hace una llamada al SP EliminarFamilia() y deposlt la
     * cantidad de registros eliminados.
     *
     * @param codigoTarifa
     */
    public void eliminarRegistro(String codigoTarifa) {
        if (JOptionPane.showConfirmDialog(null,
                "¿Realmente desea eliminar el registro?",
                "Confirme..",
                JOptionPane.YES_NO_OPTION) != JOptionPane.YES_OPTION) {
            return;
        } // end if

        boolean eliminado = false;

        try {
            eliminado = this.ivController.delete(codigoTarifa);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage(), Bitacora.ERROR);
        } // end catch

        if (eliminado) {
            JOptionPane.showMessageDialog(
                    null,
                    "Registro eliminado satisfactoriamente",
                    "Mensaje",
                    JOptionPane.INFORMATION_MESSAGE);

            txtcodigoTarifa.setText(" ");
            txtporcentaje.setText("0.00");

            skipCombo = true;
            fillCombo();
            skipCombo = false;

            txtDescrip.setText(" ");
        } // end if

    } // end eliminar

