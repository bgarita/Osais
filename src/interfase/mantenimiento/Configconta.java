    /**
     * Este método vigila que la longitud del campo sea 12.
     * Permite que el campo esté vacío; se puede tener una cuenta en blanco
     * pero no una cuenta incorrecta.
     * @param field JTextField campo que se revisará
     * @return 
     */
    private boolean checkLength(JTextField field) {
        boolean rightLength = true;
        field.setText(field.getText().trim());
        // Se permite un campo vacío
        if (field.getText().trim().isEmpty()){
            return rightLength;
        } // end if
        if (field.getText().length() != 12){
            JOptionPane.showMessageDialog(null, 
                    "La longitud de la cuenta no es apropiada.\n" +
                    "Esta debe ser de 12 dígitos.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            field.requestFocusInWindow();
            rightLength = false;
        } // end if
        return rightLength;
    } // end checkLength

    private void setNombreCuenta(JFormattedTextField field, JLabel nombreCuenta) {
        // Se permite una cuenta vacía.  Por eso se omite el resto de la
        // validación.
        if (field.getText().trim().isEmpty()){
            return;
        } // end if
        
        field.setForeground(Color.red);
        
        cta.setCuentaString(field.getText().trim());
        if (cta.isError()){
            JOptionPane.showMessageDialog(null, 
                    cta.getMensaje_error(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } // end if
        if (cta.getNom_cta().isEmpty()){
            JOptionPane.showMessageDialog(null, 
                    "Cuenta no existe.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } // end if
        if (field.getText().trim().length() < 12){
            field.setText(cta.getCuentaString());
        }
        if (cta.getNivel() == 0){
            JOptionPane.showMessageDialog(null, 
                    "Esta no es una cuenta de movimientos.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            nombreCuenta.setText("");
            return;
        } // end if
        nombreCuenta.setText(cta.getNom_cta());
        Color c = new Color(60,60,60);
        field.setForeground(c);
    } // end setNombreCuenta

    private void guardarRegistro() throws SQLException, SQLInjectionException {
        // Obtengo los tipos de asiento para compras y ventas
        short tipo_comp_V  = getTipoCompFromCombo(this.cboDescripAsientoV);
        short tipo_comp_C  = getTipoCompFromCombo(this.cboDescripAsientoC);
        short tipo_comp_PC = getTipoCompFromCombo(this.cboDescripAsientoPC);    // Pagos clientes
        short tipo_comp_PP = getTipoCompFromCombo(this.cboDescripAsientoPP);    // Pagos proveedores
        
        String sqlSent = "Select ventas_g from configcuentas";
        PreparedStatement ps = conn.prepareStatement(sqlSent);
        ResultSet rs = CMD.select(ps);
        boolean existe = (rs != null && rs.first());
        
        ps.close();
        
        if (existe){
            sqlSent = 
                    "Update configcuentas Set " +
                    "   ventas_g = ?,    " +
                    "   ventas_e = ?,    " +
                    "   descuento_vg = ?," +
                    "   descuento_ve = ?," +
                    "   transitoria = ?, " +
                    "   compras_g = ?,   " +
                    "   compras_e = ?,   " +
                    "   ctaCierre = ?,   " +
                    "   mesCierreA = ?,  " +
                    "   tipo_comp_V = ?, " +
                    "   tipo_comp_C = ?, " +
                    "   tipo_comp_P = ?, " +
                    "   tipo_comp_PP = ?," +
                    "   mostrarfechaRep = ?";
        } else {
            sqlSent = 
                    "Insert into configcuentas (" + 
                    "   ventas_g,    " +
                    "   ventas_e,    " +
                    "   descuento_vg," +
                    "   descuento_ve," +
                    "   transitoria, " +
                    "   compras_g,   " +
                    "   compras_e,   " +
                    "   ctaCierre,   " +
                    "   mesCierreA,  " +
                    "   tipo_comp_V, " +
                    "   tipo_comp_C, " +
                    "   tipo_comp_P, " +
                    "   tipo_comp_PP," +
                    "   mostrarfechaRep) " +
                    "   Values(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)"; 
        } // end if
        
        ps = conn.prepareStatement(sqlSent);
        ps.setString(1, txtVentas_g.getText());
        ps.setString(2, txtVentas_e.getText());
        ps.setString(3, txtDescuento_vg.getText());
        ps.setString(4, txtDescuento_ve.getText());
        ps.setString(5, txtBanco.getText());
        ps.setString(6, txtCompras_g.getText());
        ps.setString(7, txtCompras_e.getText());
        ps.setString(8, txtCtaCierre.getText());
        ps.setInt(9, cboCierreFiscal.getSelectedIndex() + 1);
        ps.setShort(10, tipo_comp_V);
        ps.setShort(11, tipo_comp_C);
        ps.setShort(12, tipo_comp_PC);
        ps.setShort(13, tipo_comp_PP);
        ps.setBoolean(14, chkmostrarfechaRep.isSelected());
        
        Ut.isSQLInjection(sqlSent);
        
        CMD.update(ps);
        
        ps.close();
    } // end guardarRegistro
    
    private void cargarRegistro(){
        String sqlSent = 
                "Select                         " +
                "        configcuentas.*,       " +
                "        coperiodoco.descrip    " +
                "from configcuentas             " +
                "Left join coperiodoco on configcuentas.mesactual = coperiodoco.mes " +
                "and configcuentas.añoactual = coperiodoco.año";
        try {
            try (PreparedStatement ps = conn.prepareStatement(sqlSent, 
                                        ResultSet.TYPE_SCROLL_SENSITIVE,
                                        ResultSet.CONCUR_READ_ONLY)) {
                ResultSet rs = CMD.select(ps);
                if (rs == null || !rs.first()){
                    return;
                } // end if
                this.txtVentas_e.setText(rs.getString("Ventas_e"));
                this.txtVentas_g.setText(rs.getString("Ventas_g"));
                this.txtCompras_e.setText(rs.getString("Compras_e"));
                this.txtCompras_g.setText(rs.getString("Compras_g"));
                this.txtDescuento_ve.setText(rs.getString("Descuento_ve"));
                this.txtDescuento_vg.setText(rs.getString("Descuento_vg"));
                this.txtBanco.setText(rs.getString("Transitoria"));
                this.txtCompras_g.setText(rs.getString("Compras_g"));
                this.txtCompras_e.setText(rs.getString("Compras_e"));
                this.txtCtaCierre.setText(rs.getString("ctaCierre"));
                this.chkprecierre.setSelected(rs.getBoolean("precierre"));
                this.chkmostrarfechaRep.setSelected(rs.getBoolean("mostrarfechaRep"));
                this.txtdescrip.setText(rs.getString("descrip"));
                this.cboCierreFiscal.setSelectedIndex(rs.getInt("mesCierreA") - 1);
                
                // Seleccionar el tipo de asiento que viene desde la base de datos
                // en el combo que corresponda.
                setComboValue(this.cboDescripAsientoV, rs.getShort("tipo_comp_V"));     // Asiento de ventas
                setComboValue(this.cboDescripAsientoC, rs.getShort("tipo_comp_C"));     // Asiento de compras
                setComboValue(this.cboDescripAsientoPC, rs.getShort("tipo_comp_P"));    // Tipo de asiento pagos a clientes
                setComboValue(this.cboDescripAsientoPP, rs.getShort("tipo_comp_PP"));   // Tipo de asiento pagos a proveedores
                ps.close();
            } // end try with resources
            
            // Ejecuto los eventos para desplegar el nombre de las cuentas
            this.txtVentas_eFocusLost(null);
            this.txtVentas_gFocusLost(null);
            this.txtCompras_eFocusLost(null);
            this.txtCompras_gFocusLost(null);
            this.txtDescuento_veFocusLost(null);
            this.txtDescuento_vgFocusLost(null);
            this.txtBancoFocusLost(null);
            this.txtCompras_gFocusLost(null);
            this.txtCompras_eFocusLost(null);
            this.txtCtaCierreFocusLost(null);
        } catch (SQLException ex) {
            Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
        } // end try-catch
        
    } // end cargarRegistro

    private void cargarTiposAsiento() {
        Cotipasient tipo = new Cotipasient(conn);
        this.aTipos = tipo.getAllTipo_comp();
        for (Cotipasient listTipo : aTipos) {
            this.cboDescripAsientoV.addItem(listTipo.getDescrip());
            this.cboDescripAsientoC.addItem(listTipo.getDescrip());
            this.cboDescripAsientoPC.addItem(listTipo.getDescrip());
            this.cboDescripAsientoPP.addItem(listTipo.getDescrip());
        } // end for
    } // end cargarTiposAsiento

    private void setComboValue(JComboBox<String> cboDescripAsiento, short tipo_comp) {
        // Localizo el índice en el array ya que ambos combos se cargaron en el mismo orden.
        cboDescripAsiento.setSelectedIndex(-1);
        
        for (int i = 0; i < this.aTipos.length; i++){
            if (aTipos[i].getTipo_comp() == tipo_comp){
                cboDescripAsiento.setSelectedIndex(i);
                break;
            } // end if
        } // end for
    } // end setComboValue

    private short getTipoCompFromCombo(JComboBox<String> cboDescripAsiento) {
        int selectedIndex = cboDescripAsiento.getSelectedIndex();
        if (selectedIndex < 0){
            return 0;
        } // end if
        return this.aTipos[selectedIndex].getTipo_comp();
    } // end getTipoCompFromCombo
