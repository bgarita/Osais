    public ConsultaFacturacionTransito(Connection c)
            throws SQLException {
        initComponents();
        
        conn = c;

        // Formateo la tabla
        FormatoTabla formato = new FormatoTabla();
        formato.setStringColor(Color.magenta);
        formato.setStringHorizontalAlignment(SwingConstants.LEFT);
        formato.getTableCellRendererComponent(tblDetalle,
                tblDetalle.getValueAt(0, 3),
                tblDetalle.isCellSelected(0, 3),
                tblDetalle.isFocusOwner(), 0, 3);

        this.tblDetalle.setDefaultRenderer(String.class, formato);

        String sqlSelect = 
                "Select          " +
                "    c.clidesc,  " +
                "    b.artdesc,  " +
                "    a.bodega,   " +
                "    a.reservado " +
                "from pedidofd a, inarticu b, inclient c " +
                "Where a.facnume = c.clicode " +
                "and a.artcode = b.artcode   ";

        PreparedStatement ps = conn.prepareStatement(sqlSelect);
        rs = ps.executeQuery();
        if (!rs.first()){
            return;
        } // end if

        // Obtengo el número de filas obtenidas en ResultSet
        rs.last();
        int totalFilas = rs.getRow();
        
        // Obtener el modelo de la tabla y establecer el número exacto
        // de filas.
        DefaultTableModel dtm = (DefaultTableModel) tblDetalle.getModel();
        dtm.setRowCount(totalFilas);
        tblDetalle.setModel(dtm);

        rs.beforeFirst();
        int row = 0;
        // Cargar los datos en la tabla
        while (rs.next()){
            tblDetalle.setValueAt(rs.getString("clidesc"), row, 0);
            tblDetalle.setValueAt(rs.getString("artdesc"), row, 1);
            tblDetalle.setValueAt(rs.getString("bodega"), row, 2);
            tblDetalle.setValueAt(rs.getDouble("reservado"), row, 3);
            row++;
        } // end while
        rs.close();

    } // end constructor
    
