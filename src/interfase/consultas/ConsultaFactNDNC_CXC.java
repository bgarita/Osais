    private void txtFacnumeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtFacnumeActionPerformed
        String sqlSent = "Call ConsultarFactNDNC(?,?)";
        int facnume;
        PreparedStatement ps;
        try {
            facnume = Integer.parseInt(txtFacnume.getText());
            ps = conn.prepareStatement(
                    sqlSent,
                    ResultSet.TYPE_SCROLL_SENSITIVE,
                    ResultSet.CONCUR_READ_ONLY);

            ps.setInt(1, facnume);
            ps.setString(2, tipoDoc);
            rs = CMD.select(ps);
            if (rs == null || !rs.first()) {
                JOptionPane.showMessageDialog(null,
                        "Documento no encontrado",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            } // end if

            // Cargar el encabezado de la factura
            txtClidesc.setText(rs.getString("clidesc"));
            lblFacfech.setText(rs.getString("facfech"));
            lblVendedor.setText(rs.getString("vendedor"));
            lblUser.setText(rs.getString("user"));
            lblTipo.setText(rs.getString("tipo"));
            lblFacestado.setText(rs.getString("facestado"));
            lblMoneda.setText(rs.getString("simbolo") + " " + rs.getString("moneda"));
            txaFactext.setText(rs.getString("texto") == null ? "" : rs.getString("texto"));
            lblMedioPago.setText(rs.getString("medioPago"));

            // Establezco el número de filas adecuado para la consulta
            rs.last();
            int dataRows = rs.getRow();
            DefaultTableModel dtm = (DefaultTableModel) tabDetalle.getModel();
            int tableRows = dtm.getRowCount();
            if (tableRows < dataRows) {
                dtm.setRowCount(dataRows);
                tabDetalle.setModel(dtm);
                tableRows = dataRows;
            }// end if

            // Inicializo todas las celdas para evitar datos sucios
            int r;
            for (r = 0; r < tableRows; r++) {
                this.tabDetalle.setValueAt(null, r, 0);
                this.tabDetalle.setValueAt(null, r, 1);
                this.tabDetalle.setValueAt(null, r, 2);
                this.tabDetalle.setValueAt(null, r, 3);
                this.tabDetalle.setValueAt(null, r, 4);
                this.tabDetalle.setValueAt(null, r, 5);
            } // end for

            // Cargar el detalle de la factura
            rs.beforeFirst();
            r = 0;
            while (rs.next() && r < tableRows) {
                this.tabDetalle.setValueAt(rs.getString("artcode"), r, 0);
                this.tabDetalle.setValueAt(rs.getString("bodega"), r, 1);
                this.tabDetalle.setValueAt(rs.getString("artdesc"), r, 2);
                this.tabDetalle.setValueAt(rs.getDouble("faccant"), r, 3);
                this.tabDetalle.setValueAt(rs.getDouble("artprec"), r, 4);
                this.tabDetalle.setValueAt(rs.getDouble("facmont"), r, 5);
                r++;
            } // end while

            // Cargar los totales de la factura (ND-NC) con formato
            rs.first();
            this.txtIV.setText(
                    Ut.fDecimal(rs.getString("facimve"), "#,##0.00"));
            this.txtFacdesc.setText(
                    Ut.fDecimal(rs.getString("facdesc"), "#,##0.00"));
            this.txtFacmont.setText(
                    Ut.fDecimal(rs.getString("Total"), "#,##0.00"));
            this.txtFacmonexp.setText(
                    Ut.fDecimal(rs.getString("facmonexp"), "#,##0.00"));
            this.txtFacsald.setText(
                    Ut.fDecimal(rs.getString("facsald"), "#,##0.00"));

            rs.close();
            ps.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            new Bitacora().writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
        }

}//GEN-LAST:event_txtFacnumeActionPerformed

