    private void cargarTabla() {
        int rows, row;
        try {
            rs.last();
            rows = rs.getRow();
            
            DefaultTableModel dtm = (DefaultTableModel) tblArticulos.getModel();
            if (dtm.getRowCount() < rows){
                dtm.setRowCount(rows);
                tblArticulos.setModel(dtm);
            } // end if
            rs.beforeFirst();
            row = 0;
            while (rs.next()){
                tblArticulos.setValueAt(rs.getString("bodega"), row, 0);
                tblArticulos.setValueAt(rs.getString("artcode"), row, 1);
                tblArticulos.setValueAt(rs.getString("artdesc"), row, 2);
                tblArticulos.setValueAt(rs.getDouble("artexis"), row, 3);
                tblArticulos.setValueAt(rs.getFloat("minimo"), row, 4);
                row++;
            } // end while
        } catch (SQLException ex) {
            Logger.getLogger(DetalleNotificacion.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(), 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            new Bitacora().writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
        } // end try-catch
    } // end cargarTabla
