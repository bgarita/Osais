    private void txtFacnumeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtFacnumeActionPerformed
        int doc = Integer.parseInt(txtFacnume.getText());
        int tipoDoc = cboTipoDoc.getSelectedIndex()+1;
        String sqlSent = 
                "Call ConsultarRegistrosCXC(?,?)";
        PreparedStatement ps;

        // Limpiar todos los datos
        txtFecha.setText("");
        lblMensaje.setText("No hay registros para esta consulta");

        // Inicializo todas las celdas para evitar datos sucios
        int r;
        for (r = 0; r < tabDetalle.getRowCount(); r++){
            this.tabDetalle.setValueAt(null, r, 0);
            this.tabDetalle.setValueAt(null, r, 1);
            this.tabDetalle.setValueAt(null, r, 2);
            this.tabDetalle.setValueAt(null, r, 3);
            this.tabDetalle.setValueAt(null, r, 4);
        } // end for

        try {
            ps = conn.prepareStatement(
                    sqlSent, 
                    ResultSet.TYPE_SCROLL_SENSITIVE, 
                    ResultSet.CONCUR_READ_ONLY);
            ps.setInt(1, doc);
            ps.setInt(2, tipoDoc);
            rs = CMD.select(ps);
            
            if (rs == null || !rs.first()) {
                return;
            } // end if

            txtFecha.setText(rs.getString("fecha"));
            lblMensaje.setText(rs.getString("mensaje"));

            // Establezco el número de filas adecuado para la consulta
            rs.last();
            int dataRows = rs.getRow();
            DefaultTableModel dtm = (DefaultTableModel) tabDetalle.getModel();
            int tableRows = dtm.getRowCount();
            if(tableRows < dataRows){
                dtm.setRowCount(dataRows);
                tabDetalle.setModel(dtm);
                tableRows = dataRows;
            }// end if

            // Cargar el nombre del cliente
            rs.first();
            this.lblClicode.setText(rs.getString("clidesc"));
            
            // Cargar el detalle de los registros
            rs.beforeFirst();
            r = 0;
            while (rs.next() && r < tableRows){
                this.tabDetalle.setValueAt(rs.getInt("aplicado"), r, 0);
                this.tabDetalle.setValueAt(rs.getString("tipo" ), r, 1);
                this.tabDetalle.setValueAt(rs.getString("simbolo"), r, 2);
                this.tabDetalle.setValueAt(rs.getDouble("monto"), r, 3);
                this.tabDetalle.setValueAt(rs.getDouble("tipoca"), r, 4);
                r++;
            } // end while
            
            ps.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            Bitacora b = new Bitacora();
            b.setLogLevel(Bitacora.ERROR);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
        } // end try-catch

}//GEN-LAST:event_txtFacnumeActionPerformed

