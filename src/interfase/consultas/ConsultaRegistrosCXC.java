    private void cboTipoDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTipoDocActionPerformed
        // Las NC deben ir negativas aunque el SP está preparado para eso.
        if (cboTipoDoc.getSelectedIndex()+1 == 2){
            txtFacnume.setText("-" + txtFacnume.getText().trim());
        }else{
            txtFacnume.setText(
                    String.valueOf(
                    Math.abs(
                    Integer.parseInt(txtFacnume.getText().trim()))));
        } // end if

        txtFacnumeActionPerformed(null);
    }//GEN-LAST:event_cboTipoDocActionPerformed


