    private void cargarProveedores() {
        String sqlSent = 
                "Select concat(procode,' - ', prodesc) " +
                "from inproved " +
                "Where Exists(Select dia from prodiavisita " +
                "               Where procode = inproved.procode " +
                "               and dia = ?)";
        Calendar cal = GregorianCalendar.getInstance();
        String hoy = Ut.hoy(cal.getTime());
        PreparedStatement ps;
        ResultSet rs;
        DefaultListModel<String> modelo = new DefaultListModel<>();
        
        try {
            ps = conn.prepareStatement(sqlSent,
                    ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ps.setString(1, hoy);
            rs = CMD.select(ps);
            while (rs.next()){
                modelo.addElement(rs.getString(1));
            } // end while
            ps.close();
            this.lstProveedoresDeHoy.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(VisitaProveedores.class.getName()).log(Level.SEVERE, null, ex);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage(), Bitacora.ERROR);
        }
    } // end cargarProveedores
