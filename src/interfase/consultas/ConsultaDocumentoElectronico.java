    private void cboFacndActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboFacndActionPerformed
        int facnume = Math.abs(Integer.parseInt(this.txtFacnume.getText().trim()));

        // Las facturas aparecerán con un cero, las NC con un número positivo y las ND con un número negativo
        int facnd = 0;  // factura
        if (this.cboFacnd.getSelectedIndex() == 1) { // NC
            facnd = facnume;
            facnume = facnume * -1;
        } else if (this.cboFacnd.getSelectedIndex() == 2) { // ND
            facnd = facnume * -1;
        }

        String tipoXML = "V";   // Solo se usa para documentos de ventas
        String accion = "1";    // 1=Envío, 2=Consultar.  Se envía 1 porque parece que el API de desisatec no funciona bien con 2
        try {
            String os = Ut.getProperty(Ut.OS_NAME).toLowerCase();
            if (!os.contains("win")) {
                JOptionPane.showMessageDialog(null,
                        "Esta opción solo corre en Windows.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            DocumentoElectronico documentoElectronico = new DocumentoElectronico(facnume, facnd, tipoXML, conn, accion);
            boolean existe = documentoElectronico.existeDoc();
            if (!existe) {
                JOptionPane.showMessageDialog(null,
                        "Documento no existe en la base de datos.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Preparo el comando a ejecutar.
            // La clase ParametrosXML.java se inicializa desde DocumetoElectronico.java
            String exe = Menu.DIR.getXmls() + "\\FE2.exe";
            String cmd[]
                    = {exe, ParametrosXML.xmlFileName,
                        ParametrosXML.document,
                        ParametrosXML.todoAction,
                        ParametrosXML.documentType,
                        ParametrosXML.companyHome,
                        ParametrosXML.documentKey,
                        ParametrosXML.ourIdType,
                        ParametrosXML.supplierIdType};

            JOptionPane.showMessageDialog(null, "Llamando al API...");
            Runtime r = Runtime.getRuntime();
            Process p = r.exec(cmd);

            // Obtener la salida del API
            InputStream out = p.getInputStream();
            byte[] allBytes = out.readAllBytes();
            String text = new String(allBytes, StandardCharsets.UTF_8);
            int endingCode = p.waitFor();

            // Si funciona pero hay que considerar que todo el texto va a aparecer dos veces (casi siempre)
            // Por eso es que el primer estado que toma es el de Procesando porque el que se debe leer es
            // el segundo.
            /*
            Entonces para resolver esto es necesario validar si el texto contiene la palabra post-env
            Si existe se debe ignorar todo el texto que haya antes.  Para eso simplemente se hace otra
            substring partiendo de ese punto hasta el final y trabajar con ese nuevo substring.
            */
            
            // Estraer el estado
            int inicial = text.indexOf("@Estado movimiento: ");
            int fin = text.indexOf("@", inicial + 1);
            this.txtEstado.setText(text.substring(inicial, fin).replace("@Estado movimiento: ", ""));

            inicial = text.indexOf("@Codigo de respuesta ");
            fin = text.indexOf("@", inicial + 1);
            this.txtCodigoRespuesta.setText(text.substring(inicial, fin).replace("@Codigo de respuesta ", ""));

            inicial = text.indexOf("@Mensaje respuesta de Hacienda: ");
            fin = text.indexOf("@", inicial + 1);
            this.jTextArea1.setText(text.substring(inicial, fin).replace("@Mensaje respuesta de Hacienda: ", ""));
            
        } catch (SQLException | IOException | InterruptedException ex) {
            JOptionPane.showMessageDialog(null,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_cboFacndActionPerformed


