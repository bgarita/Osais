    private void cmdEstaLineaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEstaLineaActionPerformed
        String desc;
        try {
            desc = Ut.quitarFormato(txtFacdesc.getText());
        } catch (Exception ex) {
            Logger.getLogger(Descuentos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(), 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
            return;
        }
        this.JTCampo.setText("1" + desc);
        dispose();
    }//GEN-LAST:event_cmdEstaLineaActionPerformed

