    private void cmdAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAceptarActionPerformed
        int porDesc     = chkOrdenarxdesc.isSelected() ? 1:0;
        int regenerar   = chkRegenerar.isSelected()    ? 1:0;
        int valoracion  = cboValoracion.getSelectedIndex();
        String localiz1 = this.txtLocaliz1.getText().trim();
        String localiz2 = this.txtLocaliz2.getText().trim();
        //        String sqlSent =
        //                "Call PrepararConteo(?," +
        //                      porDesc      + "," +
        //                      regenerar    + "," + // Sobreescribir la tabla (1=Si,0=No)
        //                      valoracion   + ")";
        String sqlSent = "Call PrepararConteo(?,?,?,?,?,?)";
        
                
        PreparedStatement ps;
        boolean hayTran = false;
        try {
            // Bosco agregado 01/06/2013.
            // Se ha encontrado que por alguna razón la tabla de conteo se 
            // afecta pero al salir del sistema en ese preciso momento ya los
            // datos han desaparecido como si se hubiera ejecutado un rollback.
            // Por ahora incluyo el control de transacciones aquí para ver que
            // resultado se obtiene y si continúa igual entonces debo incluir
            // un commit antes de iniciar la transacción.
            CMD.transaction(conn, CMD.START_TRANSACTION);
            hayTran = true;
            ps = conn.prepareStatement(
                    sqlSent, ResultSet.TYPE_SCROLL_SENSITIVE, 
                    ResultSet.CONCUR_READ_ONLY);
            ps.setString(1, bodega);
            ps.setInt(2, porDesc);
            ps.setInt(3, regenerar);
            ps.setInt(4, valoracion);
            ps.setString(5, localiz1);
            ps.setString(6, localiz2);
            // Fin Bosco agregado 01/06/2013.
            
            // El SP devuelve un rs con el número de filas afectadas.
            //ResultSet filas = stat.executeQuery(query);
            ResultSet filas = CMD.select(ps);
            if (filas == null || !filas.first() || filas.getInt(1) == 0){
                JOptionPane.showMessageDialog(
                    null,
                    "No se generaron datos para el conteo \n"
                    + "Informe al DBA.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
                CMD.transaction(conn, CMD.ROLLBACK);
                return;
            } // end if

            // Informo sobre la cantidad de registros listos para el conteo
            JOptionPane.showMessageDialog(
                    null,
                    "Se generaron " + filas.getInt(1) +
                    " artículos para conteo.",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
            CMD.transaction(conn, CMD.COMMIT);
            ps.close();
            hayTran = false;
        } catch (SQLException ex) {
            Logger.getLogger(TablaConteo.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(
                    null,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
            if (hayTran){
                try {
                    CMD.transaction(conn, CMD.ROLLBACK);
                } catch (SQLException ex1) {
                    Logger.getLogger(TablaConteo.class.getName()).log(Level.SEVERE, null, ex1);
                    JOptionPane.showMessageDialog(
                            null,
                            ex1.getMessage(),
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                    b.writeToLog(this.getClass().getName() + "--> " + ex1.getMessage());
                } // end try-catch interno
            }
        } // end try-catch

    }//GEN-LAST:event_cmdAceptarActionPerformed


