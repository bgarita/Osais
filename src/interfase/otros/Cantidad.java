    private void txtFaccantKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFaccantKeyPressed
        if(evt.getKeyCode() != java.awt.event.KeyEvent.VK_ENTER){
            return;
        } // end if
        
        // Validar que el dato se un número
        try {
            float nCantidad = Float.parseFloat(txtFaccant.getText().trim());
            this.cantidad.setText(nCantidad + "");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(this, 
                    "Debe digitar una cantidad válida.", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            new Bitacora().writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
            return;
        } // end try-cacth
        
        
        setVisible(false);
        dispose();
    }//GEN-LAST:event_txtFaccantKeyPressed

