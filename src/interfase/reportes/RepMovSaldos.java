    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String artcode1, artcode2,
                bodega1, bodega2,
                artfam1, artfam2,
                movfech1,movfech2,
                orden,query,filtro;

        artcode1 = txtArtcode1.getText().trim();
        artcode2 = txtArtcode2.getText().trim();
        bodega1  = txtBodega1.getText().trim();
        bodega2  = txtBodega2.getText().trim();
        artfam1  = txtArtfam1.getText().trim();
        artfam2  = txtArtfam2.getText().trim();
        movfech1 = Ut.fechaSQL(DatMovfech1.getDate());
        movfech2 = Ut.fechaSQL(DatMovfech2.getDate());
        orden    = String.valueOf(cboOrden.getSelectedIndex());

        query =
            "Call Rep_Saldos(" +
            "'" + artcode1 + "'" + "," +
            "'" + artcode2 + "'" + "," +
            "'" + bodega1  + "'" + "," +
            "'" + bodega2  + "'" + "," +
            "'" + artfam1  + "'" + "," +
            "'" + artfam2  + "'" + "," +
                  movfech1       + "," +
                  movfech2       + "," +
                  orden    + ")";
        try {
            if (Ut.isSQLInjection(query)){
                return;
            } // end if
        } catch (SQLInjectionException ex) {
            Logger.getLogger(RepMovSaldos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
            return;
        } // end try-catch

        filtro = "Rangos del reporte: ";
        if (artcode1.isEmpty() && artcode2.isEmpty()){
            filtro += "Todos los artículos, ";
        }else{
            filtro += "Artículos del " + artcode1 + " al " +
                    artcode2 + ", ";
        } // end if

        if (bodega1.isEmpty() && bodega2.isEmpty()){
            filtro += "Todos las bodegas, ";
        }else{
            filtro += "Bodegas del " + bodega1 + " al " +
                    bodega2 + ", ";
        } // end if

        if (artfam1.isEmpty() && artfam2.isEmpty()){
            filtro += "Todas las familias, ";
        }else{
            filtro += "Familias del " + artfam1 + " al " +
                    artfam2 + ", ";
        } // end if

        filtro += "Fechas de " + movfech1 + " a " + movfech2;

//        new Reportes(conn).generico(
//                query,
//                "",     // where
//                "",     // Order By
//                filtro,
//                "RepMovSaldos.jasper",
//                "Movimientos y saldos (conciliación)");
        ReportesProgressBar rpb = 
                new ReportesProgressBar(
                conn, 
                "Movimientos y saldos (conciliación)",
                "RepMovSaldos.jasper",
                query,
                filtro);
        rpb.setBorderTitle("Calculando saldos..");
        rpb.setVisible(true);
        rpb.start();
    }//GEN-LAST:event_cmdImprimirActionPerformed

