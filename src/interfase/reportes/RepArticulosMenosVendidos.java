/**
 * Extraer los artículos que menos venta tengan durante un período de 
 * tiempo. Las notas de crédito restan a las ventas.  No se consideran aquellos
 * artículos que presenten fecha de compra en ese período de análisis ni tampco
 * aquellos artículos que no tengan existencia.
 * El objetivo de este reporte es presentar los artículos que menos se venden
 * para deshecharlos.
 * 
 * @param evt 
 */
    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String  meses,topX,query,filtro,jasperForm;

        meses = this.spnMeses.getValue().toString();
        topX  = this.spnTopx.getValue().toString();
                
        jasperForm = "RepArticulosMenosVendidos.jasper";

        filtro = "Meses procesados: " + meses + ", Top " + topX;

        query = "Select " +
                "	a.artcode," +
                "	c.artdesc," +
                "	sum(a.faccant) as unidades, " +
                "(Select empresa from config) as Empresa " +
                "from fadetall a " +
                "Inner join faencabe b on a.facnume = b.facnume and a.facnd = b.facnd " +
                "Inner join inarticu c on a.artcode = c.artcode " +
                "Where a.facnd >= 0 " +
                "and b.facfech >= (now() - interval " + meses + " month) " +
                "and (b.facestado is null or b.facestado = '') " +
                "and c.artfeuc is not null " +
                "and c.artfeuc <= (now() - interval " + meses + " month) " +
                "group by a.artcode,	c.artdesc " +
                "union all " +
                "Select  " +
                "	a.artcode, " +
                "	a.artdesc, " +
                "	0 as unidades, " +
                "(Select empresa from config) as Empresa " +
                "from inarticu a " +
                "Where artexis > 0 " +
                "and a.artfeuc is not null " +
                "and a.artfeuc <= (now() - interval " + meses + " month) " +
                "and not exists( " +
                "	Select c.artcode from fadetall c " +
                "	Inner join faencabe d on c.facnume = d.facnume and c.facnd = d.facnd " +
                "	Where c.artcode = a.artcode  " +
                "	and c.facnd >= 0 " +
                "	and d.facfech >= (now() - interval " + meses + " month) " +
                "	and (d.facestado is null or d.facestado = '') " +
                "	) " +
                "order by unidades " +
                "limit " + topX;

        ReportesProgressBar rpb = 
                new ReportesProgressBar(
                conn, 
                "Artículos menos vendidos",
                jasperForm,
                query,
                filtro);
        rpb.setBorderTitle("Consultado base de datos..");
        rpb.setVisible(true);
        rpb.start();
    }//GEN-LAST:event_cmdImprimirActionPerformed

