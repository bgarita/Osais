    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String artcode1, artcode2, bodega1, bodega2, excluir, margen,
                orden, tipoRep, precio, calculo, query, titulo;
        int aplicaOferta;
        artcode1 = txtArtcode1.getText().trim();
        artcode2 = txtArtcode2.getText().trim();
        bodega1  = txtBodega1.getText().trim();
        bodega2  = txtBodega2.getText().trim();
        aplicaOferta = (this.chkSoloOferta.isSelected()? 1 : 0); // Bosco agregado 12/03/2014
        
        titulo = "";
        
        if (bodega1.equals(bodega2)){
            try {
                titulo = 
                        UtilBD.getDBString(conn, "bodegas", "bodega = '" + bodega1 + "'" , "descrip");
            } catch (NotUniqueValueException | SQLException ex) {
                Logger.getLogger(RepExistenciasPB.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(
                        null,
                        ex.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                new Bitacora().writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
                return;
            } // end try-catch
        } // end if

        // 1 = Excluir artículos sin existencia
        // 2 = Excluir articulos con existencia
        // 3 = no excluir
        excluir  =
                !radExcluirSE.isSelected()?
                 radExcluirCE.isSelected()? "2" : "3" : "1";

        // Establecer el margen de utilidad a procesar
        margen   = txtMargen.getText().trim();
        margen   = margen.substring(0,margen.length()-1);
        margen   = margen.trim();
        if (margen.equals(".")) {
            margen = "0";
        } // end if
        
        orden   = String.valueOf(cboOrden.getSelectedIndex());
        tipoRep = String.valueOf(cboTipoRep.getSelectedIndex());
        precio  = spnArtprec.getValue().toString();

        // 0=Costo total, 1=Costo unitario
        calculo = cboCosto.getSelectedIndex() == 0 ? "0":"1";
        
        query = "Call Rep_Existencias(" +
        "'" + artcode1 + "'" + "," +
        "'" + artcode2 + "'" + "," +
        "'" + bodega1  + "'" + "," +
        "'" + bodega2  + "'" + ","
            + excluir  + ","
            + margen   + ","
            + orden    + ","
            + calculo  + "," +
              aplicaOferta +")";
        try {
            Ut.isSQLInjection(query);
        } catch (NumberFormatException | SQLInjectionException ex) {
            Logger.getLogger(RepExistenciasPB.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            new Bitacora().writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
            return;
        } // end try-catch

        new Reportes(conn).existenciasPB(tipoRep, precio, query, titulo);
    }//GEN-LAST:event_cmdImprimirActionPerformed

