    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        if (this.cboMesA.getSelectedIndex() == this.cboMesB.getSelectedIndex()
                && this.txtYearA.getText().trim().equals(this.txtYearB.getText().trim())) {
            JOptionPane.showMessageDialog(null,
                    "Los periodos a comparar deben ser distintos",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } // end if

        int year = Integer.parseInt(this.txtYearA.getText().trim());
        Cocatalogo coca = new Cocatalogo(conn);
        String error_message;
        if (!coca.existePeriodoCerrado(year)) {
            error_message
                    = coca.getMensaje_error().isEmpty()
                    ? this.cboMesA.getSelectedItem() + ", " + year + " no existe."
                    : coca.getMensaje_error();

            JOptionPane.showMessageDialog(null,
                    error_message,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } // end if

        year = Integer.parseInt(this.txtYearB.getText().trim());
        if (!coca.existePeriodoCerrado(year)) {
            error_message
                    = coca.getMensaje_error().isEmpty()
                    ? this.cboMesB.getSelectedItem() + ", " + year + " no existe."
                    : coca.getMensaje_error();

            JOptionPane.showMessageDialog(null,
                    error_message,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } // end if

        String sqlSent;
        int yearA = Integer.parseInt(this.txtYearA.getText().trim());
        int yearB = Integer.parseInt(this.txtYearB.getText().trim());
        int monthA = this.cboMesA.getSelectedIndex() + 1;
        int monthB = this.cboMesB.getSelectedIndex() + 1;
        String cuentaA = this.txtCuentaA.getText().trim();
        String cuentaB = this.txtCuentaB.getText().trim();
        String nivel = this.radMayor.isSelected() ? "0" : (this.radMov.isSelected() ? "1":"");
        
        if (!nivel.isEmpty()){
            nivel = " AND a.nivel = " + nivel;
        }

        sqlSent
                = "SELECT "
                + "	a.mayor, "
                + "	a.sub_cta, "
                + "	a.sub_sub, "
                + "	a.colect, "
                + "	a.nom_cta, "
                + "	if(a.nivel = 0, 'Mayor', 'Detalle') as nivel ,"
                + "	a.db_mes - a.cr_mes AS saldoMesA,"
                + "	("
                + "		SELECT b.db_mes - b.cr_mes "
                + "		FROM hcocatalogo b "
                + "		WHERE b.mayor = a.mayor "
                + "		AND b.sub_cta = a.sub_cta "
                + "		AND b.sub_sub = a.sub_sub "
                + "		AND b.colect  = a.colect  "
                + "		AND YEAR(b.fecha_cierre)  = " + yearB
                + "		AND MONTH(b.fecha_cierre) = " + monthB
                + "	) AS saldoMesB, "
                + "    (Select mostrarFechaRep from configcuentas) as mostrarFecha   "
                + "FROM hcocatalogo a "
                + "WHERE concat(a.mayor, a.sub_cta, a.sub_sub, a.colect) BETWEEN '" + cuentaA + "' AND '" + cuentaB + "' "
                + "AND YEAR(a.fecha_cierre)  = " + yearA  + " "
                + "AND MONTH(a.fecha_cierre) = " + monthA + " "
                + nivel + " "
                + "ORDER BY 1,2,3,4";

        try {

            String formJasper = "RepComparativoMensual.jasper";
            String periodo1 = this.cboMesA.getSelectedItem() + " " + yearA;
            String periodo2 = this.cboMesB.getSelectedItem() + " " + yearB;
            
            String filtro 
                    = "Comparativo " + periodo1 + " Vs " + periodo2 
                    + ". Cuentas: " + cuentaA + " - "    + cuentaB;

            new Reportes(conn).CGComparativoMensual(
                    sqlSent,
                    "", // where
                    "", // Order By
                    filtro, // filtro
                    formJasper,
                    periodo1,
                    periodo2);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
        } // end try-catch
    }//GEN-LAST:event_btnImprimirActionPerformed

