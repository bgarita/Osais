    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String facfech1,facfech2,orden,query,filtro,formJasper;

        facfech1 = Ut.fechaSQL(datFecha1.getDate());
        facfech2 = Ut.fechaSQL(datFecha2.getDate());
        orden    = String.valueOf(cboOrden.getSelectedIndex());
        formJasper = "RepDocumentosXTipo.jasper";

        filtro = "Rangos del reporte: ";


        filtro += "Fechas de " + Ut.dtoc(datFecha1.getDate()) +
                " a " + Ut.dtoc(datFecha2.getDate()) + ".";

        query = "Call Rep_DocumentosXTipo(" + facfech1 + "," + facfech2 + "," + orden + ")";

        
        ReportesProgressBar rpb =
            new ReportesProgressBar(
                sConn,
                "Listado de documentos de inventario por tipo",
                formJasper,
                query,
                filtro);
        rpb.setBorderTitle("Consultado base de datos..");
        rpb.setVisible(true);
        rpb.start();
    }//GEN-LAST:event_cmdImprimirActionPerformed

