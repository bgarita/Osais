    private void cargarFamilias() {
        String sqlSent = 
                "Select concat(artfam, '-', familia) as familia "
                + "from infamily "
                + "Union Select concat('0000', '-', 'Todas las familias') "
                + "order by 1";
        PreparedStatement ps;
        ResultSet rs;
        try {
            ps = conn.prepareStatement(sqlSent, 
                    ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            rs = CMD.select(ps);
            
            Ut.fillComboBox(cboArtfam, rs, 1, false);
            ps.close();
        } catch (SQLException | EmptyDataSourceException ex) {
            Logger.getLogger(RepArticulosXProveedor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(), 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage(), Bitacora.ERROR);
        } // end try-catch
    } // end cargarFamilias
    
