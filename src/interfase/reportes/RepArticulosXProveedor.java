    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        String  procode1,procode2,artfam,familia,
                orderBy,query,filtro,where,
                jasperForm;

        procode1 = txtProcode1.getText().trim();
        procode2 = txtProcode2.getText().trim();
        
        artfam = this.cboArtfam.getSelectedItem().toString();
        familia = artfam.substring(Ut.getPosicion(artfam, "-") + 1);
        artfam = artfam.substring(0, Ut.getPosicion(artfam, "-"));
        

        orderBy  = " Order by ";
        
        switch (cboOrden.getSelectedIndex()){
            case 0: orderBy += " artcode "; break;
            case 1: orderBy += " artdesc "; break;
            case 2: orderBy += " prodesc "; break;
            case 3: orderBy += " artexis "; break;
            default: orderBy += " artcode ";
        } // end switch
        
        jasperForm = "RepArticulosXProveedor.jasper";

        filtro = "Rangos del reporte: ";

        filtro += "Proveedores del " + procode1 + " al " + procode2 + ",";
        
        filtro += artfam.equals("0000") ? " " + familia : "Familia: " + familia;

        query = "Select " +
                "	artcode, " +
                "	artdesc, " +
                "	artexis, " +
                "       artmini, " +
                "	artcost, " +
                "	prodesc, " +
                "	If(length(protel2) > 1,concat(protel1,'/',protel2), protel1) as telefonos, " +
                "       (Select empresa from config) as Empresa " +
                "from inarticu a " +
                "Inner join inproved b on a.procode = b.procode" ;
        
        where = " Where a.procode ";
        
        if (!procode1.isEmpty() && !procode2.isEmpty()){
            where += " between " + "'" + procode1 + "' and '" + procode2 + "'";
        } else if (!procode1.isEmpty()){
            where += ">= " + "'" + procode1 + "'";
        } else if (!procode2.isEmpty()){
            where += "<= " + "'" + procode2 + "'";
        } else {
            where = "";
        } // end if-else-if
        
        if (!artfam.equals("0000")){
            if (where.isEmpty()){
                where += " Where ";
            } else {
                where += " and ";
            } // end if-else
            where += "a.artfam = '" + artfam + "'";
        } // end if
        
        query += where + orderBy;
        
        try {
            Ut.isSQLInjection(query);
        } catch (SQLInjectionException ex) {
            Logger.getLogger(RepArticulosXProveedor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            b.writeToLog(this.getClass().getName() + "--> " + ex.getMessage(), Bitacora.ERROR);
            return;
        } // end try-catch
        
        ReportesProgressBar rpb = 
                new ReportesProgressBar(
                conn, 
                this.getTitle(),
                jasperForm,
                query,
                filtro);
        rpb.setBorderTitle("Consultando base de datos..");
        rpb.setVisible(true);
        rpb.start();
    }//GEN-LAST:event_btnImprimirActionPerformed

