    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String  facfech,orden,
                query,filtro,
                formJasper;

        facfech = Ut.fechaHoraSQL(DatFacfech.getDate(),"23:59:59");
        
        orden   = String.valueOf(cboOrden.getSelectedIndex());
        formJasper = "RepEstadodeLasCXC.jasper";

        filtro = "Estado de las cuentas por cobrar al: ";

        filtro += Ut.dtoc(DatFacfech.getDate()) + ".";

        query = "Call Rep_EstadoDeLasCXC(" +
                facfech + "," +
                orden   + ")";
         
        new Reportes(conn).generico(
                query,
                "",     // where
                "",     // Order By
                filtro,
                formJasper,
                "Estado de las cuentas por cobrar");
    }//GEN-LAST:event_cmdImprimirActionPerformed

