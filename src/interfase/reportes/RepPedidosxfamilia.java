    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String  artfam1, artfam2,
                clientes,soloPNC,
                orden,query,filtro,
                formJasper;

        artfam1  = txtArtfam1.getText().trim();
        artfam2  = txtArtfam2.getText().trim();
        // 0=Todas.  Para el SP ""=Todas.
        artfam1  = artfam1.equals("0") ? "" : artfam1;
        artfam2  = artfam2.equals("0") ? "" : artfam2;
        clientes = radClientesTodos.isSelected() ? "3" : radClientesContado.isSelected() ? "1" : "2";
        soloPNC  = chkSoloPNC.isSelected() ? "1" : "0";
        orden    = String.valueOf(cboOrden.getSelectedIndex());
        formJasper = "RepPedidosxfamilia.jasper";

        filtro = "Rangos del reporte: ";

        if ((artfam1.isEmpty() && artfam2.isEmpty()) ||
                (artfam1.trim().equals("0") && artfam2.trim().equals("0"))){
            filtro += "Todas las familias, ";
        }else{
            filtro += "Familias de " + artfam1 + " a " +
                    artfam2 + ", ";
        } // end if

        filtro += "Clientes: ";
        filtro += radClientesTodos.isSelected() ? "Todos" :
            radClientesContado.isSelected() ? "de contado" : "de crédito";
        filtro += chkSoloPNC.isSelected() ? ", sólo pedidos no cubiertos" : "";
       
        query = "Call Rep_PedidosxFamilia(" +
                "'" + artfam1 + "'" + "," +
                "'" + artfam2 + "'" + "," +
                      clientes      + "," +
                      soloPNC       + "," +
                      orden    + ")";
         
        new Reportes(conn).generico(
                query,
                "",     // where
                "",     // Order By
                filtro,
                formJasper,
                "Pedidos por familia");
    }//GEN-LAST:event_cmdImprimirActionPerformed

