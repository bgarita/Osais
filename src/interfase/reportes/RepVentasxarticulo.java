    private void mnuBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuBuscarActionPerformed
        JTextField objeto;
        switch (objetoBusqueda){
            case 1:
                objeto = txtArtcode1;
                break;
            case 2:
                objeto = txtArtcode2;
                break;            
            default:
                objeto = null;
        } // end switch

        if (objeto == null){
            JOptionPane.showMessageDialog(null,
                    "Debe seleccionar alguno de los " +
                    "campos de artículo " +
                    "\nantes de usar la opción de búsqueda.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        } // end if

        bd = new Buscador(new java.awt.Frame(), true,
                "inarticu", "artcode,artdesc", "artdesc",
                objeto,conn);
        bd.setTitle("Buscar artículos");
        
        bd.lblBuscar.setText("Artículo:");
        bd.setVisible(true);
        bd.dispose();
}//GEN-LAST:event_mnuBuscarActionPerformed

