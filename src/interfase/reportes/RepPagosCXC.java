    private void txtClicode2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtClicode2FocusLost
        if (txtClicode2.getText() == null ||
                txtClicode2.getText().trim().equals("0")){
            return;
        } // end if

        String clicode1 = txtClicode1.getText().trim();
        String clicode2 = txtClicode2.getText().trim();
        if (clicode1.compareTo(clicode2) > 0){
            JOptionPane.showMessageDialog(null,
                    "Rango incorrecto de clientes.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            txtClicode1.requestFocusInWindow();
        } // end if
}//GEN-LAST:event_txtClicode2FocusLost


