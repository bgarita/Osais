    private void cmdImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdImprimirActionPerformed
        String fechaven1,
                fechaven2,
                Select,
                Where = "",
                OrderBy,
                filtro = "";
        fechaven1 = Ut.fechaSQL(DatMovfech1.getDate());
        fechaven2 = Ut.fechaSQL(DatMovfech2.getDate());

        filtro = "Del ";
        filtro += Ut.dtoc(DatMovfech1.getDate());
        filtro += " al ";
        filtro += Ut.dtoc(DatMovfech2.getDate());

        // Rango de fechas.  Establezco la fechas en esta parte
        // del Where para que SQL utilice los índices.
        if (!fechaven1.equals("null")){
            Where = "a.fechaven >= " + fechaven1;
        } // end if

        if (!fechaven2.equals("null")){
            Where = Where.isEmpty() ? Where : Where + " and ";
            Where += "a.fechaven <= " + fechaven2;
        } // end if

        Where = Where.isEmpty() ? "c.estado = '' ": Where + "and c.estado = '' ";

        OrderBy = "ORDER BY ";

        switch (cboOrden.getSelectedIndex()){
            case 0:
                OrderBy += "artcode";
                break;
            case 1:
                OrderBy += "artdesc";
                break;
            case 2:
                OrderBy += "bodega";
                break;
            case 3:
                OrderBy += "movdocu";
                break;
            case 4:
                OrderBy += "movcant";
                break;
            case 5:
                OrderBy += "movfech";
                break;
            case 6:
                OrderBy += "fechaven";
                break;
            default: OrderBy += "fechaven";
        } // end switch

        Select =
            "SELECT             " +
            "  (Select empresa from config) as empresa, " +
            "  a.artcode,       " +
            "  b.artdesc,       " +
            "  a.bodega ,       " +
            "  a.movdocu,       " +
            "  a.movcant,       " +
            "  c.movfech,       " +
            "  a.fechaven       " +
            "FROM inmovimd a    " +
            "INNER JOIN inarticu b ON a.artcode = b.artcode     " +
            "INNER JOIN inmovime c ON a.movdocu = c.movdocu and " +
            "                         a.movtimo = c.movtimo and " +
            "                         a.movtido = c.movtido     " +
            "Where " + Where +
            OrderBy;

        // En este caso envío toda la sentencia en el Select
        // pero podría enviar todo por separado.
//        new Reportes(conn).generico(
//                Select,
//                "",     // where
//                "",     // Order By
//                filtro,
//                "RepVencimientosInv.jasper",
//                "Vencimientos de inventario");
        
        ReportesProgressBar rpb = 
                new ReportesProgressBar(
                conn, 
                "Vencimientos de inventario",
                "RepVencimientosInv.jasper",
                Select,
                filtro);
        rpb.setBorderTitle("Consultado base de datos..");
        rpb.setVisible(true);
        rpb.start();
    }//GEN-LAST:event_cmdImprimirActionPerformed

