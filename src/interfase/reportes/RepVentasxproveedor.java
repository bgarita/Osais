    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        String  artfam1, artfam2,
                facfech1,facfech2,
                orden,query,filtro,
                formJasper;

        artfam1  = txtArtfam1.getText().trim();
        artfam2  = txtArtfam2.getText().trim();
        facfech1 = Ut.fechaSQL(DatFacfech1.getDate());
        facfech2 = Ut.fechaSQL(DatFacfech2.getDate());
        orden    = String.valueOf(cboOrden.getSelectedIndex());
        
        formJasper = "RepVentasxproveedor.jasper";
        
        if (!orden.equals("0")){
            formJasper = "RepVentasxproveedor2.jasper";
        } // end if
        

        filtro = "Rangos del reporte: ";

        if ((artfam1.isEmpty() && artfam2.isEmpty()) ||
                (artfam1.trim().equals("0") && artfam2.trim().equals("0"))){
            filtro += "Todas las familias, ";
        }else{
            filtro += "Familias de " + artfam1 + " a " +
                    artfam2 + ", ";
        } // end if

        filtro += "Fechas de " + Ut.dtoc(DatFacfech1.getDate()) +
                " a " + Ut.dtoc(DatFacfech2.getDate()) + ".";
       
        query = "Call Rep_Ventasxproveedor(" +
                facfech1 + "," +
                facfech2 + "," +
                "'" + artfam1 + "'" + "," +
                "'" + artfam2 + "'" + "," +
                orden    + ")";
        try {
            // Esta rutina lanza una excepción cuando encuentra inyección de código.
            Ut.isSQLInjection(query);
        } catch (SQLInjectionException ex) {
            Logger.getLogger(RepVentasxproveedor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, 
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            new Bitacora().writeToLog(this.getClass().getName() + "--> " + ex.getMessage());
            return;
        } // end try-catch
        

        ReportesProgressBar rpb = 
                new ReportesProgressBar(
                conn, 
                "Listado de ventas por proveedor",
                formJasper,
                query,
                filtro);
        rpb.setBorderTitle("Procesando registros..");
        rpb.setVisible(true);
        rpb.start();
    }//GEN-LAST:event_btnImprimirActionPerformed

    private void DatFacfech2PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_DatFacfech2PropertyChange
        if (inicio || fin){
            return;
        } // end if
        
        Date fecha1 = DatFacfech1.getDate();
        Date fecha2 = DatFacfech2.getDate();
        
        if (fecha1 == null) {
            return;
        } // end if
        
        if (fecha1.after(fecha2)){
            JOptionPane.showMessageDialog(null,
                    "Rango incorrecto de fechas.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            DatFacfech1.requestFocusInWindow();
        } // end if
    }//GEN-LAST:event_DatFacfech2PropertyChange

