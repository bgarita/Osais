    private void txtArtfam2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtArtfam2FocusLost
        if (txtArtfam2.getText() == null ||
                txtArtfam2.getText().trim().equals("0")){
            return;
        } // end if

        String artfam1 = txtArtfam1.getText().trim();
        String artfam2 = txtArtfam2.getText().trim();
        
        if (artfam1.compareTo(artfam2) > 0){
            JOptionPane.showMessageDialog(null,
                    "Rango incorrecto de familias.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            txtArtfam1.requestFocusInWindow();
        } // end if
}//GEN-LAST:event_txtArtfam2FocusLost

